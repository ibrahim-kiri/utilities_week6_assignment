void main() {
  String fruit = 'tomato';

  switch (fruit) {
    case 'apple':
      print('Selected fruit is an apple.');
      break;
    case 'banana':
      print('Selected fruit is a banana.');
      break;
    case 'orange':
      print('Selected fruit is an orange.');
      break;
    default:
      print('Selected fruit is not recognized.');
  }
}
